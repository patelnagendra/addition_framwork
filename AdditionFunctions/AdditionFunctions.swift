import Foundation
public class AdditionFunctions {
    public static func add(_ a: Int, _ b: Int) -> Int {
        return a + b
    }
}

public class ReductionFunctions {
    public static func add(_ a: Int, _ b: Int) -> Int {
        return a - b
    }
}
